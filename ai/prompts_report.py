def smart_truncate(text: str, max_chars: int) -> str:
    """按行截断文本，保护 Markdown 表格完整性。

    不会在表格行（以 | 开头）中间截断：
    - 如果截断点落在表格内部，回退到表格开始前截断
    - 追加 "[...数据截断]" 提示
    """
    if not text or len(text) <= max_chars:
        return text
    lines = text.split('\n')
    result = []
    total = 0
    in_table = False
    last_safe_idx = 0  # 表格外的最后一个安全截断点

    for i, line in enumerate(lines):
        line_len = len(line) + 1
        is_table_line = line.strip().startswith("|")

        if is_table_line and not in_table:
            in_table = True
            last_safe_idx = len(result)  # 表格开始前
        elif not is_table_line and in_table:
            in_table = False

        if total + line_len > max_chars:
            if in_table and last_safe_idx > 0:
                result = result[:last_safe_idx]
            result.append("[...数据截断]")
            break
        result.append(line)
        total += line_len

    return '\n'.join(result) if result else text[:max_chars]


