def smart_truncate(text: str, max_chars: int) -> str:
    """按行截断文本，确保不截断表格行中间。"""
    if not text or len(text) <= max_chars:
        return text
    lines = text.split('\n')
    result = []
    total = 0
    for line in lines:
        line_len = len(line) + 1  # +1 for newline
        if total + line_len > max_chars:
            break
        result.append(line)
        total += line_len
    return '\n'.join(result) if result else text[:max_chars]


